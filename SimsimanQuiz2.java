public class SimsimanQuiz2 {
    public static void main(String[] args) {
        int num1 = 125;
        int num2 = 24;
        int op1 = num1 + num2;
        int op2 = num1 - num2; 
        int op3 = num1 * num2;
        int op4 = num1 / num2;
        int op5 = num1 % num2;

        System.out.println(num1 + " + " + num2 + " = " + op1);
        System.out.println(num1 + " - " + num2 + " = " + op2);
        System.out.println(num1 + " x " + num2 + " = " + op3);
        System.out.println(num1 + " / " + num2 + " = " + op4);
        System.out.println(num1 + " mod " + num2 + " = " + op5);
    }
    
}
